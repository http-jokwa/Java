package Conditional_Statement;

public class if_Statement {

	public static void main(String[] args) {
	
		/*
		 * 		if (condition is true) {
		 * 			print
		 * 		}
		 * 
		 * We will use the logic 
		 * 
		 */
		
		int x = 10, y = 20;
		
		if(x < y) { //this is true because 10 is less than 20
			
			System.out.println(x + " is less than " + y); 	//output : 10 is less than 20
			
		}
		
		// we will use else if the condition is false.

		
	}

}
