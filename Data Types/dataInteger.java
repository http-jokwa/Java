package DataTypes;

public class dataInteger {

	public static void main(String[] args) {
		
		/*Integer datatype uses only a number
		 * you can't use double quotation mark
		 * has maximum of 9 digits
		 */
		
		//variable name should not be duplicate
		int number1 = 1;
		int number2 = 2;
		int number3 = 231313123; 
		
		
		System.out.println(number1); //output: 1
		System.out.println(number2); //output: 2
		System.out.println(number3); //output: 231313123
	}

}
