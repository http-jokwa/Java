package DataTypes;

public class dataCharacter {

	public static void main(String[] args) {
		
		/*
		 * Character data types is only signle character
		 * 
		 * To identify if the data type is character
		 * is by using single quotation mark (' ')
		 */
		
		char letter = 'a';
		char letter1 = 'A';
		char number = '2'; 
		char specialchar = '@';
		
		System.out.println(letter); //output : a
		System.out.println(letter1); //output : A
		System.out.println(number); //output : 2
		System.out.println(specialchar); //output : @
		
		
		/*
		 * As you can see we have number 2 here and usually it is integer data type
		 * but when a number is surround by a single quotation mark it will become character data type
		 * 
		 * When a number is surrounded by a double quotation mark
		 * it will become a String data type
		 */
		
	}

}
