package DataTypes;

public class dataFloat {

	public static void main(String[] args) {
		
		/*
		 * Float or floating point is a number
		 * with decimal.
		 * Use lower f after the number
		 */
		
		float num1 = 1;
		float num2 = 2f; //correct way of declaring a value for float
		
		System.out.println(num1); //output: 1.0
		System.out.println(num2); //output: 2.0
		

	}

}
