package DataTypes;

public class String_UpperLower_Case {

	public static void main(String[] args) {
		/*
		 * Upper Case = make all the string into Upper Case
		 * Lower Case = make all the string into Lower Case
		 */
		
		String name = "Juan";
		String name1 = "Mang Kanor";
		
		System.out.println(name.toUpperCase()); //output : JUAN
		System.out.println(name1.toLowerCase()); //output : mang kanor
		
	}

}
