package DataTypes;

public class DisplayCharString {

	public static void main(String[] args) {
		
		/*
		 * We will display a single letter of String
		 * by calling the index
		 * 
		 * Counting always starts at zero 0
		 */
		
		String name = "Sophia";
		
		System.out.println(name.charAt(0)); //output : S
		System.out.println(name.charAt(1)); //output : o
		System.out.println(name.charAt(2)); //output : p
		System.out.println(name.charAt(3)); //output : h
		System.out.println(name.charAt(4)); //output : i
		System.out.println(name.charAt(5)); //output : a
	}

}
