package DataTypes;

public class Length_of_String {

	public static void main(String[] args) {
		/*
		 * Calculate the number of letter and white space
		 */
		
		String name = "Jovit Baldivino";
		String fname = "Juan";
		
		System.out.println(name.length()); //output : 15
		System.out.println(fname.length()); //output : 4
	}

}
