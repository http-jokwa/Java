package SwitchCase;

public class Switch_Statement {

	public static void main(String[] args) {
		
		
		int number = 2;
		
		switch (number) {
		
		case 1 : 
			System.out.println("This is number " + number);
			break;
			
		case 2 :
			System.out.println("This is number " + number);
			break;
			
		default :	
			System.out.println("This number is greater than 1 and 2 ");
			break;	
		}
		
	}

}
