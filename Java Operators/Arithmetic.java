package Operators;

public class Arithmetic {

	public static void main(String[] args) {
	
		/*
		 * Arithmetic Operator used to perform arithmetic operations
		 * + Addition
		 * - Subtraction
		 * * Multiplication
		 * / Division
		 * % Modulo (Remainder)
		 */

		int x = 20, y = 30;
		
		//addition
		System.out.println(x + y); // output : 50
		
		//subtraction
		System.out.println(x - y); // output : -10
		
		//multiplication
		System.out.println(x * y); // output : 600
		
		//division
		System.out.println(x / y); //output : 0
		
		//remainder or modulo
		System.out.println(10 % 3); // output : 1
		
	}

}
