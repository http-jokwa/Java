public class OneDimension {
  
 public static void main(String [] args) { 
  

   int [] number = {1,2,3,4,5};
   
   //to call the element, arrayname[index]
   
    System.out.println(number[0]); //output : 1
    System.out.println(number[1]); //output : 2
    System.out.println(number[2]); //output : 3
    System.out.println(number[3]); //output : 4
    System.out.println(number[4]); //output : 5
   
   
   //you can also reassign the element value of array
   
    number[0] = 12; //now the index zero with the value of 1 will now 12
   System.out.println(number[0]); //output : 12
   
 }
}
