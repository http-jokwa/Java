package Loops;

public class forEach {
    public static void main(String args[]) {
         
         String [] names = {"Joschua", "Ordonio", "BSIT"};
         
			
			
         for(String val : names ) {
             System.out.println(val);
         }
			
			/* 
					for(Data type for container : Array name) {
							System.out.println(container name);	
						}
			
			
			For each loop will print all the element 
			inside the array.
			If you are confused in identifying the size of 
			array, use for each loop.
			*/
    }
}