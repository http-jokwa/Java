package Loops;

public class whileLoop {

	public static void main(String[] args) {
		

		/*
		 * Loop = run your code continuously
		 * as long as the condition is true.
		 * 
		 */
		
		int number = 1;
		
		while(number<10) {
			System.out.println(number + " is less than 10");
			number++; // we increment to change the value
		}
		
		/*
		 * Because 1 is less than 10 and that is true that's why
		 * loop continue. 
		 * 
		 * We use increment to change the value of number from 1 change to 2
		 * and so on. Once the number incremented to 10 our condition will be now false, why?
		 * is 10 less than 10? that is false thats why our program end.
		 * 
		 * 
		 * Output
		 * 
			1 is less than 10
			2 is less than 10
			3 is less than 10
			4 is less than 10
			5 is less than 10
			6 is less than 10
			7 is less than 10
			8 is less than 10
			9 is less than 10
			
		 */

	}

}
