package Loops;

public class forLoop {

	public static void main(String[] args) {
		
		
		//	initialize,     condition  increment or decrement
		for(int number = 0; number<=5; number++) {
			System.out.println("Hello");
		}
		
		/*
		 * 1st. initialize the variable
		 * 2nd check the condition if true
		 * 3rd if true then it will print "Hello" 
		 * after printing, our initialized variable will be incremented
		 * from 0 it will become 1 and check again the condition...
		 * until our condition will become false.
		 * 
		 * OUTPUT
		 * 
		 * Hello
		 * Hello
		 * Hello
		 * Hello
		 * Hello
		 */
	}

}
