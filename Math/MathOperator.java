package Math;

public class MathOperator {

	public static void main(String[] args) {
		
		/*
		 * Operators are;
		 * + = Addition
		 * - = Subtraction
		 * * = for Multiplication
		 * / = for Division
		 * % = for Remainder (Modulo)
		 * 
		 */
		
		int x = 10;
		int z = 12;
		
		System.out.println(x + z); //output : 22
		System.out.println(x - z); //output : -2
		System.out.println(x * z); //output : 120
		System.out.println(x / z); //output : 0
		System.out.println(x % 3); //output : 1  Remainder
		System.out.println(z % 5); //output : 2  Remainder

	}

}
